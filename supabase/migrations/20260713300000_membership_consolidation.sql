  v_tbl          record;
  v_fk           record;
  v_pass         integer := 0;
  v_progress     boolean;
  v_cnt          bigint;
  v_remaining    bigint;
  v_report       jsonb;
begin
  create temp table _protected_users on commit drop as
    select u.id
      from auth.users u
     where lower(u.email) = any (select lower(e) from unnest(v_protected_emails) e)
        or coalesce((u.raw_user_meta_data->>'is_super_admin')::boolean, false) = true
    union
    select pu.id from public.users pu where pu.is_super_admin is true
    union
    select om.user_id from public.organization_members om
     where om.role = 'super_admin' and om.user_id is not null;

  create temp table _members on commit drop as
    select organization_id as org_id, user_id from public.organization_members where user_id is not null
    union
    select organization_id, id from public.users where organization_id is not null;

  create temp table _org_stats on commit drop as
    select o.id   as org_id,
           o.name,
           o.organization_type,
           count(distinct m.user_id)               as member_count,
           max(au.last_sign_in_at)                 as last_sign_in,
           bool_or(p.id is not null)               as has_protected_member
      from public.organizations o
      left join _members m         on m.org_id = o.id
      left join auth.users au      on au.id = m.user_id
      left join _protected_users p on p.id = m.user_id
     group by o.id, o.name, o.organization_type;

  create temp table _candidates on commit drop as
    select org_id
      from _org_stats
     where organization_type <> 'internal'
       and has_protected_member = false
       and (last_sign_in is null or last_sign_in < v_cutoff);

  create temp table _org_tables on commit drop as
    select distinct t.table_schema, t.table_name, x.column_name
      from (
            select c.table_schema, c.table_name, c.column_name
              from information_schema.columns c
             where c.table_schema = 'public'
               and c.column_name in ('organization_id', 'org_id')
            union
            select tc.table_schema, tc.table_name, kcu.column_name
              from information_schema.table_constraints tc
              join information_schema.key_column_usage kcu
                on kcu.constraint_name = tc.constraint_name
               and kcu.constraint_schema = tc.constraint_schema
              join information_schema.constraint_column_usage ccu
                on ccu.constraint_name = tc.constraint_name
               and ccu.constraint_schema = tc.constraint_schema
             where tc.constraint_type = 'FOREIGN KEY'
               and ccu.table_schema = 'public'
               and ccu.table_name = 'organizations'
           ) x
      join information_schema.tables t
        on t.table_schema = x.table_schema
       and t.table_name = x.table_name
       and t.table_type = 'BASE TABLE'
     where x.table_name <> 'organizations';

  create temp table _fk on commit drop as
    select tc.table_schema  as child_schema,  tc.table_name  as child_tbl,  kcu.column_name as child_col,
           ccu.table_schema as parent_schema, ccu.table_name as parent_tbl, ccu.column_name as parent_col
      from information_schema.table_constraints tc
      join information_schema.key_column_usage kcu
        on kcu.constraint_name = tc.constraint_name and kcu.constraint_schema = tc.constraint_schema
      join information_schema.constraint_column_usage ccu
        on ccu.constraint_name = tc.constraint_name and ccu.constraint_schema = tc.constraint_schema
     where tc.constraint_type = 'FOREIGN KEY'
       and tc.table_schema = 'public'
       and ccu.table_schema = 'public'
       and tc.constraint_name in (
             select constraint_name
               from information_schema.key_column_usage
              where constraint_schema = 'public'
              group by constraint_name
             having count(*) = 1
           );

  create temp table _affected (org_id uuid, table_name text, column_name text, rows bigint)
    on commit drop;

  for v_tbl in select table_schema, table_name, column_name from _org_tables loop
    execute format(
      'insert into _affected (org_id, table_name, column_name, rows)
         select %1$I, %2$L, %3$L, count(*)
           from %4$I.%2$I
          where %1$I in (select org_id from _candidates)
          group by %1$I',
      v_tbl.column_name, v_tbl.table_name, v_tbl.column_name, v_tbl.table_schema
    );
  end loop;

  create temp table _auth_to_delete on commit drop as
    select distinct m.user_id
      from _members m
     where m.org_id in (select org_id from _candidates)
       and m.user_id is not null
       and m.user_id not in (select id from _protected_users)
       and not exists (
             select 1 from _members m2
              where m2.user_id = m.user_id
                and m2.org_id not in (select org_id from _candidates)
           );

  if not p_dry_run then
    set local session_replication_role = 'replica';

    for v_tbl in select table_schema, table_name, column_name from _org_tables loop
      execute format(
        'delete from %1$I.%2$I where %3$I in (select org_id from _candidates)',
        v_tbl.table_schema, v_tbl.table_name, v_tbl.column_name
      );
    end loop;

    v_pass := 0;
    loop
      v_pass := v_pass + 1;
      v_progress := false;

      for v_fk in select * from _fk loop
        execute format(
          'delete from %1$I.%2$I c
             where c.%3$I is not null
               and not exists (select 1 from %4$I.%5$I p where p.%6$I = c.%3$I)',
          v_fk.child_schema, v_fk.child_tbl, v_fk.child_col,
          v_fk.parent_schema, v_fk.parent_tbl, v_fk.parent_col
        );
        get diagnostics v_cnt = row_count;
        if v_cnt > 0 then
          v_progress := true;
        end if;
      end loop;

      exit when v_progress = false;
      exit when v_pass >= 50;
    end loop;

    delete from public.users where id in (select user_id from _auth_to_delete);

    delete from public.organizations where id in (select org_id from _candidates);

    set local session_replication_role = 'origin';

    for v_tbl in select table_schema, table_name, column_name from _org_tables loop
      execute format(
        'select count(*) from %1$I.%2$I where %3$I in (select org_id from _candidates)',
        v_tbl.table_schema, v_tbl.table_name, v_tbl.column_name
      ) into v_remaining;

      if v_remaining > 0 then
        raise exception
          'Purge incomplete: % row(s) remain in %.% (column %). '
          'Rolling back — no changes committed.',
          v_remaining, v_tbl.table_schema, v_tbl.table_name, v_tbl.column_name;
      end if;
    end loop;
  end if;

  select jsonb_build_object(
    'dry_run', p_dry_run,
    'inactivity_days', p_inactivity_days,
    'cutoff', v_cutoff,
    'summary', jsonb_build_object(
      'candidate_org_count', (select count(*) from _candidates),
      'total_dependent_rows', coalesce((select sum(rows) from _affected), 0),
      'auth_users_to_delete', (select count(*) from _auth_to_delete)
    ),
    'candidate_orgs', coalesce((
      select jsonb_agg(jsonb_build_object(
        'org_id', s.org_id,
        'name', s.name,
        'organization_type', s.organization_type,
        'member_count', s.member_count,
        'last_sign_in', s.last_sign_in,
        'dependent_rows', coalesce((select sum(a.rows) from _affected a where a.org_id = s.org_id), 0),
        'members', coalesce((
          select jsonb_agg(jsonb_build_object(
            'user_id', m.user_id,
            'email', au.email,
            'last_sign_in_at', au.last_sign_in_at
          ) order by au.email)
          from _members m
          join auth.users au on au.id = m.user_id
          where m.org_id = s.org_id
        ), '[]'::jsonb)
      ) order by s.name)
      from _org_stats s
      where s.org_id in (select org_id from _candidates)
    ), '[]'::jsonb),
    'tables_affected', coalesce((
      select jsonb_agg(jsonb_build_object('table', table_name, 'column', column_name, 'rows', total)
                       order by total desc)
      from (
        select table_name, column_name, sum(rows) as total
          from _affected
         group by table_name, column_name
        having sum(rows) > 0
      ) t
    ), '[]'::jsonb),
    'auth_users_to_delete', coalesce((
      select jsonb_agg(jsonb_build_object(
        'id', d.user_id, 'email', au.email, 'last_sign_in_at', au.last_sign_in_at
      ) order by au.email)
      from _auth_to_delete d
      join auth.users au on au.id = d.user_id
    ), '[]'::jsonb),
    'excluded_orgs', coalesce((
      select jsonb_agg(jsonb_build_object(
        'org_id', s.org_id,
        'name', s.name,
        'reason', case
                    when s.organization_type = 'internal' then 'internal org'
                    when s.has_protected_member then 'has protected/super-admin member'
                    else 'active within cutoff'
                  end)
        order by s.name)
      from _org_stats s
      where s.org_id not in (select org_id from _candidates)
    ), '[]'::jsonb)
  ) into v_report;

  return v_report;
end;
$$;

-- Dead / one-off functions that only existed for the legacy tables:
--   handle_hse_signup    — orphaned (no trigger references it; the only
--                          auth.users trigger is handle_new_user, which
--                          already writes organization_members +
--                          organization_apps) and it inserts into the frozen
--                          organization_users, so it could never run anyway.
--   migrate_petrolord_users / migrate_suite_users_to_hse — one-shot legacy
--                          data fixers for organization_users.
drop function if exists public.handle_hse_signup();
drop function if exists public.migrate_petrolord_users();
drop function if exists public.migrate_suite_users_to_hse();

-- ----------------------------------------------------------------------------
-- 5. Drop the stragglers. Policies ON these tables (and the deprecation
--    triggers) go with them. Plain DROP (no CASCADE) so any remaining
--    dependency aborts the migration loudly instead of silently dropping
--    dependent objects.
-- ----------------------------------------------------------------------------
drop table public.organization_users;
drop table public.org_members;
drop function if exists public.block_organization_users_writes();
drop function if exists public.block_org_members_writes();

-- ----------------------------------------------------------------------------
-- 6. Name-compatible read-only views so the currently-deployed production SPA
--    (which still reads organization_users / org_members directly) keeps
--    working until the next prod upload. security_invoker => the querying
--    user's organization_members RLS applies, matching the legacy tables'
--    own-row/own-org visibility. Views with computed columns are not
--    auto-updatable, so legacy writes keep failing — same as under the old
--    deprecation triggers. DROP these in a follow-up migration once the
--    consolidated frontend build is live in production.
-- ----------------------------------------------------------------------------
create view public.organization_users
with (security_invoker = true) as
select
  om.id,
  om.organization_id,
  om.user_id,
  coalesce((select array_agg(distinct oa.module_id)
              from public.organization_apps oa
             where oa.organization_id = om.organization_id
               and oa.module_id is not null), array[]::text[]) as modules,
  om.role,
  om.created_at,
  coalesce((select array_agg(distinct oa.app_id)
              from public.organization_apps oa
             where oa.organization_id = om.organization_id
               and oa.app_id is not null), array[]::text[]) as apps,
  om.role as user_role,
  om.status,
  om.updated_at::timestamp as updated_at
from public.organization_members om;

comment on view public.organization_users is
  'COMPATIBILITY VIEW (membership consolidation 20260713300000): read-only '
  'projection of organization_members for the pre-consolidation production '
  'SPA. Do not use in new code — query organization_members / helpers. '
  'Scheduled to be dropped after the next production upload.';

create view public.org_members
with (security_invoker = true) as
select
  om.id,
  om.organization_id as org_id,
  om.user_id,
  om.role,
  om.invited_at,
  om.joined_at,
  null::uuid as invited_by
from public.organization_members om;

comment on view public.org_members is
  'COMPATIBILITY VIEW (membership consolidation 20260713300000): read-only '
  'projection of organization_members. Do not use in new code. Scheduled to '
  'be dropped after the next production upload.';

-- Supabase default privileges auto-grant full DML on new objects, and the
-- simple columns of these views are auto-updatable — force them read-only.
revoke all on public.organization_users from anon, authenticated, service_role;
revoke all on public.org_members from anon, authenticated, service_role;
grant select on public.organization_users to anon, authenticated, service_role;
grant select on public.org_members to anon, authenticated, service_role;

comment on table public.organization_members is
  'THE canonical membership table (consolidation 20260713300000). Entitlements '
  'live in organization_apps / purchased_modules; RLS must use is_org_member/'
  'has_org_role/has_any_org_role/my_org_id/is_org_admin_of, never this table '
  'directly.';

commit;
