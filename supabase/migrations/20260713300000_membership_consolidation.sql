-- Live-state audit findings this migration is built on (2026-07-13):
--   * organization_users: 1 row missing from organization_members (backfilled
--     below); org_members: 0 rows.
--   * 162 RLS policies across public/hse/storage referenced the legacy tables
--     directly in their expressions (the "one function" story only held for
--     geoscience/EPE/econ). They are rewritten here onto SECURITY DEFINER
--     helpers — generated by tools/consolidation/gen_policy_rewrites.py from
--     the live catalog dump (tools/consolidation/catalog/), audited, and
--     inlined below. 3 petrolord.* policies are out of scope (the drilling
--     module's own petrolord.org_members is a different table).
--   * ~20 SQL functions referenced the legacy tables; rewritten or dropped
--     below. handle_new_user (the only auth.users trigger) already writes
--     canonical tables and needs no change.
--
-- Role-vocabulary note: the legacy tables kept admin-ness in a separate
-- user_role column (org creators: role='owner', user_role='org_admin').
-- organization_members has a single role column where creators are 'owner',
-- so every rewritten role array also accepts 'owner'. Helper role checks
-- also require status='active' (canonical semantics; legacy checks were
-- mostly status-blind but every live legacy row is 'active').
