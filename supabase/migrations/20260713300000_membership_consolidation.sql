begin;

-- ----------------------------------------------------------------------------
-- 1. Backfill legacy membership rows into organization_members.
--    full_name/email come from auth.users (organization_members requires
--    them); role carries over; joined_at falls back to the legacy created_at.
-- ----------------------------------------------------------------------------
insert into public.organization_members
  (organization_id, user_id, full_name, email, role, status, joined_at,
   created_at, updated_at)
select
  ou.organization_id,
  ou.user_id,
  coalesce(au.raw_user_meta_data->>'full_name', au.email, 'Unknown'),
  coalesce(au.email, 'unknown+' || ou.user_id || '@petrolord.invalid'),
  coalesce(ou.role, 'member'),
  coalesce(lower(ou.status), 'active'),
  coalesce(ou.created_at, now()),
  coalesce(ou.created_at, now()),
  now()
from public.organization_users ou
left join auth.users au on au.id = ou.user_id
where not exists (
  select 1 from public.organization_members m
  where m.organization_id = ou.organization_id and m.user_id = ou.user_id
)
on conflict (organization_id, email) do nothing;

insert into public.organization_members
  (organization_id, user_id, full_name, email, role, status, joined_at,
   created_at, updated_at)
select
  om.org_id,
  om.user_id,
  coalesce(au.raw_user_meta_data->>'full_name', au.email, 'Unknown'),
  coalesce(au.email, 'unknown+' || om.user_id || '@petrolord.invalid'),
  coalesce(om.role, 'member'),
  'active',
  coalesce(om.joined_at, om.invited_at, now()),
  coalesce(om.joined_at, om.invited_at, now()),
  now()
from public.org_members om
left join auth.users au on au.id = om.user_id
where om.user_id is not null and om.org_id is not null
  and not exists (
    select 1 from public.organization_members m
    where m.organization_id = om.org_id and m.user_id = om.user_id
  )
on conflict (organization_id, email) do nothing;

-- Backfill completeness assert: every legacy (org, user) pair must now exist
-- canonically. Aborts the whole migration if not.
do $$
declare v_missing integer;
begin
  select count(*) into v_missing from (
    select ou.organization_id as org, ou.user_id as usr
      from public.organization_users ou where ou.user_id is not null
    union
    select om.org_id, om.user_id
      from public.org_members om
     where om.user_id is not null and om.org_id is not null
  ) legacy
  where not exists (
    select 1 from public.organization_members m
    where m.organization_id = legacy.org and m.user_id = legacy.usr
  );
  if v_missing > 0 then
    raise exception 'membership backfill incomplete: % legacy pair(s) missing from organization_members', v_missing;
  end if;
end $$;

-- ----------------------------------------------------------------------------
-- 2. Canonical membership helpers. SECURITY DEFINER (postgres owns the table,
--    so these bypass organization_members RLS — no policy recursion), STABLE,
--    pinned search_path. These are the ONLY membership predicates RLS may use.
-- ----------------------------------------------------------------------------
create or replace function public.is_org_member(org_id uuid)
returns boolean
language sql stable security definer
set search_path to 'public'
as $$
  select exists (
    select 1 from public.organization_members om
    where om.organization_id = is_org_member.org_id
      and om.user_id = auth.uid()
      and coalesce(lower(om.status), 'active') = 'active'
  );
$$;
comment on function public.is_org_member(uuid) is
  'True when auth.uid() is an active member of the org (organization_members '
  'is the single canonical membership table since 20260713300000). The ONLY '
  'membership predicate RLS policies may use, together with has_org_role/'
  'has_any_org_role/my_org_id/is_org_admin_of.';

create or replace function public.has_org_role(org_id uuid, roles text[])
returns boolean
language sql stable security definer
set search_path to 'public'
as $$
  select exists (
    select 1 from public.organization_members om
    where om.organization_id = has_org_role.org_id
      and om.user_id = auth.uid()
      and coalesce(lower(om.status), 'active') = 'active'
      and om.role = any (has_org_role.roles)
  );
$$;
comment on function public.has_org_role(uuid, text[]) is
  'Role-gated membership check against the canonical organization_members '
  'table. Rewritten legacy policies preserve their original role arrays plus '
  '''owner'' (see 20260713300000 header).';

create or replace function public.has_any_org_role(roles text[])
returns boolean
language sql stable security definer
set search_path to 'public'
as $$
  select exists (
    select 1 from public.organization_members om
    where om.user_id = auth.uid()
      and coalesce(lower(om.status), 'active') = 'active'
      and om.role = any (has_any_org_role.roles)
  );
$$;

create or replace function public.my_org_id()
returns uuid
language sql stable security definer
set search_path to 'public'
as $$
  select om.organization_id
  from public.organization_members om
  where om.user_id = auth.uid()
    and coalesce(lower(om.status), 'active') = 'active'
  order by om.joined_at nulls last, om.created_at
  limit 1;
$$;
comment on function public.my_org_id() is
  'Null-safe "my organization" scalar for RLS (replaces the legacy '
  '(SELECT organization_id FROM organization_users ... LIMIT 1) pattern; '
  'unlike get_my_organization_id() it never raises).';

create or replace function public.is_org_admin_of(check_org_id uuid)
returns boolean
language sql stable security definer
set search_path to 'public'
as $$
  select public.has_org_role(check_org_id,
                             array['owner','admin','org_admin','super_admin']);
$$;

